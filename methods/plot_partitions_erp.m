    title(tit);

    Ax = subplot(5, 2, 10);
    generate_erp_plot(results, start, endtime, high3.data, electrode, "none", cis, "positive");
    h = get(gca, 'Children');
    delete(h(4));
    delete(h(5));
    delete(h(6));
    delete(h(7));
